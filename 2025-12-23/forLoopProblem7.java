
// WAJP to print sum of all the numbers from range 1 to 10

public class forLoopProblem7 {
    public static void main(String[] args) {
        System.out.println("Sum of all numbers from 1 to 10:");
        int sum = 0;
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }
        System.out.println(sum);
    }
}

