// program to print all primitive type datatypes

class primitive{
	public static void main(String[] args){
		byte a = 32;
		short b = 150;
		int c = 489393208;
		long d = 48328472929l;
		float e = 2;
		double f = 3.22;
		char g = 'a';
		boolean h = true;

		System.out.println("A (" + a + ") is of byte type.");
		System.out.println("B (" + b + ") is of short type.");
		System.out.println("C (" + c + ") is of int type.");
		System.out.println("D (" + d + ") is of long type.");
		System.out.println("E (" + e + ") is of float type.");
		System.out.println("F (" + f + ") is of double type.");
		System.out.println("G (" + g + ") is of char type.");
		System.out.println("H (" + h + ") is of boolean type.");
	}
}
